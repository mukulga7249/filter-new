$(document).ready(function () {
    // Fetch cast details using the cast name from the query parameter
    var urlParams = new URLSearchParams(window.location.search);
    var castName = urlParams.get('name');
    
    // Assume 'castDetails' is the array containing cast details
    
    var selectedCastDetails = getCastDetailsByName(castName);

    // Update the cast details content on the page
    displayCastDetails(selectedCastDetails);
});

function getCastDetailsByName(name) {
    const foundCast = castDetails.find(cast => cast.name === name);
    return foundCast || {};
}

function displayCastDetails(castDetails) {
    var castDetailsContainer = $('#cast-details-content');

    var imageElement = $('<img class="cast-details-image" src="' + castDetails.image + '" alt="' + castDetails.name + '">');
    castDetailsContainer.append(imageElement);

    castDetailsContainer.append('<h2>' + castDetails.name + '</h2>');
    castDetailsContainer.append('<p>Also Known As: ' + castDetails.alsoKnownAs + '</p>');
    castDetailsContainer.append('<p>Occupation: ' + castDetails.occupation + '</p>');
    castDetailsContainer.append('<p>Born: ' + castDetails.born + '</p>');
    castDetailsContainer.append('<p>Birthplace: ' + castDetails.Birthplace + '</p>');
    castDetailsContainer.append('<p>Spouse: ' + castDetails.Spouse + '</p>');
}
